# Temperature Conversion: Python program that converts temperature from celcius to farenheit.
# Celsius To Fahrenheit
c = float(input("Enter temperature in celsius: ")) # c variable to take input form User 
fahrenheit = (c * 9/5) + 32  # Using coversion formula transformation take place
print('%.4f Celsius is: %0.4f Fahrenheit' %(c, fahrenheit)) # This statment is to show output to the screen